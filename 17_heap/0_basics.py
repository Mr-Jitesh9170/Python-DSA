# Heap => 
# 1) heap is complete binary tree, here all the data represented in form of tree.
# 2) heap is divided into two types
#    i) min heap.
#   ii) max heap.
     
# Min-Heap=>
# min heap rules say that , root element always would be less or equal to its childrens.

# Max-Heap=> 
# max heap rules say that , root element always would be greater or equal to its children.
  
  

# Note =>
# 1) while the representing data in form of tree, follow rules of left-right represantation.
