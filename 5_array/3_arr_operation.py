#3 Performing operations in array=>
#i)append => to insert the value in array.
#ii)pop() => it will removed data from end in array.
#iii)remove() => this method will be used to remove particuler data from array.
#iv)reverse() => to reverse the array.
#v)sort() => to sort the array.

#Example-1
arr=[];

#addition.
arr.append(1);
arr.append(2);
arr.append(3);
print(arr)

#deletion from last.
arr.pop();
print(arr)

#remove particuler element.
arr.remove(1)
print(arr)